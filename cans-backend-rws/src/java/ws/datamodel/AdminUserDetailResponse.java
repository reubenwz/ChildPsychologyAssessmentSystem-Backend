package ws.datamodel;

/**
 *
 * @author Ooi Jun Hao
 */
public class AdminUserDetailResponse {
    private String dob;
    private String name;
    private String gender;

    public AdminUserDetailResponse(String dob, String name, String gender) {
        this.dob = dob;
        this.name = name;
        this.gender = gender;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }
    
}
